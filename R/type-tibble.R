#' @rdname df_ptype2
#' @export
tib_ptype2 <- function(x, y, ..., x_arg = "", y_arg = "") {
  .Call(
    vctrs_tib_ptype2,
    x = x,
    y = y,
    x_arg = x_arg,
    y_arg = y_arg
  )
}
#' @rdname df_ptype2
#' @export
tib_cast <- function(x, to, ..., x_arg = "", to_arg = "") {
  .Call(
    vctrs_tib_cast,
    x = x,
    to = to,
    x_arg = x_arg,
    to_arg = to_arg
  )
}

df_as_tibble <- function(df) {
  class(df) <- c("tbl_df", "tbl", "data.frame")
  df
}

